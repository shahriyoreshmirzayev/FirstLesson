namespace ConsoleApp1._3_dars
{
    internal class AddAttributes
    {
        public int Add(int num1, int num2)
        {
            return num1 + num2;
        }
        public double Sub(int num1, int num2)
        {
            return num1 - num2;
        }
        public int Multiply(int num1, int num2)
        {
            return num1 * num2;
        }
        public int Div(int num1, int num2)
        {
            return num1 / num2;
        }
        public int Abs(int num1)
        {
            return Math.Abs(num1);
        }
        public int Pow(int num1)
        {
            return num1 * num1;
        }
    }
}
